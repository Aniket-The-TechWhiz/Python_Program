#Type Conversion
'''
Type conversion means converting of data type automatically
eg :- 
a=10 # It's integer
b=2.5 #It's Float
print(a+b) 
in this case the intger value may covert into the float ie. a= 10.0
10.0+2.5 = 12.5
'''
a=10
b=2.5
print(a+b)

#Type Casting
'''
Type Casting is done manually
eg :-
we can convert the one data type to another data type manually
c=int("2") # Type casting String to integer
d=10
print(c+d)
output :-12
'''
c=int("2") # Type casting String to integer
d=10
print(c+d)
